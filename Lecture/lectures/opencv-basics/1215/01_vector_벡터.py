# 데이터
# 학생 정보 = [나이, 키, 몸무게, 성적] = [20, 175, 70, 85]

import numpy as np

# Numpy = Numerical Python
# 수학 연산을 빠르고 쉽게 해주는 라이브러리
# 특히 벡터, 행렬 연산에 최적화되어 있음

v1 = np.array([1,2,3])
print(v1)

# 0으로 채운 벡터
v2 = np.zeros(5)
print(v2)

# 벡터 정보 확인
# 벡터의 크기 (원소 개수)
print(len(v2))
print(v2.shape)

# 원소별 곱셈
a = np.array([1,2,3])
b = np.array([2,4,6])

print( a * b)

# 벡터의 크기 = 원점에서 해당 점까지의 거리
# 2차원 벡터 [3, 4]의 크기: 
# 피타고라스 정리 사용
# √(3² + 4²) = √(9 + 16) = √25 = 5


v = np.array([3, 4])
# 방법 1: 직접 계산
크기 = np.sqrt(v[0]**2 + v[1]**2)
print(크기)

# 방법 2: numpy 함수 사용(권장)
크기 = np.linalg.norm(v)
print(크기)

# 다차원 벡터도 같은 방법
v3 = np.array([1,2,2])
크기 = np.sqrt(v3[0]**2 + v3[1]**2 + v3[2]**2)
print(크기)
크기 = np.linalg.norm(v3)
print(크기)

# 단위 벡터(정규화)

# 단위 벡터: 크기가 1인 벡터
# 방향은 유지하고 크기만 1로 만듬
print()

v = np.array([3, 4])

# 정규화: 벡터를 크기로 나눔
단위벡터 = v / np.linalg.norm(v)
print(단위벡터)

print(np.linalg.norm(단위벡터))


# AI에서 벡터의 활용
# 학생데이터 
# [나이, 키(cm), 몸무게(kg), 시험점수]
학생1 = np.array([20, 175, 70, 85])
학생2 = np.array([22, 168, 55, 92])
학생3 = np.array([21, 180, 80, 78])

# 여러 학생 = 벡터들의 집합
학생들 = np.array([학생1, 학생2, 학생3])
print(학생들.shape) # (3, 4) 학생 수 = 3 , 특성 4

# 유사도 계산 (코사인 유사도)
def 코사인유사도(a,b):
    return np.dot(a,b) / (np.linalg.norm(a) * np.linalg.norm(b))

# 예: 영화 취향 벡터
# [액션, 로맨스, 코미디, 공포, SF]
사용자A = np.array([5, 1, 3, 0, 5])  # 액션, SF 좋아함
사용자B = np.array([4, 0, 2, 1, 4])  # 비슷한 취향
사용자C = np.array([0, 5, 4, 0, 1])  # 로맨스 좋아함

print(f'A와 B 유사도: {코사인유사도(사용자A, 사용자B)}')
print(f'A와 C 유사도: {코사인유사도(사용자A, 사용자C)}')
print(f'B와 C 유사도: {코사인유사도(사용자B, 사용자C)}')

# 거리 계산
# 두 점 사이의 거리 = 벡터 뺄셈 후 크기
# 유클리드 거리 (Euclidean Distance) 계산
점1 = np.array([1, 2])
점2 = np.array([4, 6])

거리 = np.linalg.norm(점2 - 점1)
print(f'두 점 사이의 거리: {거리:.2f}')

# 벡터의 내적 (Dot Product)
# 두 벡터의 유사도를 측정하는 데 사용
# a · b = |a| × |b| × cos(θ)
# θ = 두 벡터 사이의 각도
a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

내적 = np.dot(a, b)
print(f'벡터 내적: {내적}')

