# ========================================
# 복합 할당 연산자 기본 예제
# ========================================

"""
복합 할당 연산자 복습:
각 연산자가 어떻게 동작하는지 단계별로 살펴보겠습니다.
"""

print("=== 복합 할당 연산자 동작 예제 ===")

a = 10                          # 초기값 설정
print(f"초기값: a = {a}")

a += 5                          # a = a + 5 = 10 + 5 = 15
print(f"a += 5 → a = {a}")       # 15

a -= 4                          # a = a - 4 = 15 - 4 = 11
print(f"a -= 4 → a = {a}")       # 11

a *= 3                          # a = a * 3 = 11 * 3 = 33
print(f"a *= 3 → a = {a}")       # 33

a /= 11                         # a = a / 11 = 33 / 11 = 3.0
print(f"a /= 11 → a = {a}")      # 3.0 (실수형)

a //= 2                         # a = a // 2 = 3.0 // 2 = 1.0
print(f"a //= 2 → a = {a}")      # 1.0 (정수 나눗셈, 소수점 버림)

a **= 200                       # a = a ** 200 = 1.0 ** 200 = 1.0
print(f"a **= 200 → a = {a}")    # 1.0 (1의 어떤 거듭제곱도 1)

a = 10                          # 새로운 값으로 재설정
a %= 3                          # a = a % 3 = 10 % 3 = 1
print(f"a = 10, a %= 3 → a = {a}")  # 1 (10을 3으로 나눈 나머지)

# ========================================
# 실습 1: 가계부 프로그램 (복합 할당 연산자 활용)
# ========================================

print("\n=== 실습 1: 가계부 프로그램 ===")

# 초기 잔액 설정
money = 300000                  # 초기 잔액 30만원
print(f"초기 잔액: {money:,}원")

# 1. 생활용품 구매 (8만원 지출)
money -= 80000                  # money = money - 80000
print(f"생활용품 구매 후: {money:,}원")

# 2. 교통비 지출 (9,000원 × 5일 = 45,000원)
money -= 9000 * 5               # money = money - (9000 * 5)
print(f"교통비 지출 후: {money:,}원")

# 3. 알바비 수입 (12만원 입금)
money += 120000                 # money = money + 120000
print(f"알바비 입금 후: {money:,}원")

# 4. 적금 이자 (20% 이자 적용)
money *= 1.2                    # money = money * 1.2
print(f"적금 이자 적용 후: {money:,}원")

# 5. 생활비 지출 (현재 잔액의 1/3 사용)
money -= money / 3              # money = money - (money / 3)
print(f"생활비 지출 후: {money:,.0f}원")

"""
money -= money / 3 설명:
1. 현재 money 값을 3으로 나눔
2. 그 결과를 현재 money에서 뺌
3. 결과적으로 원래 금액의 2/3가 남게 됨

예: money가 300이면
money -= money / 3
money = money - (money / 3)
money = 300 - (300 / 3)
money = 300 - 100 = 200
"""

