# ========================================
# 문자열 분리: split() 함수
# ========================================

print("\n=== 문자열 분리: split() 함수 ===")

"""
split() 함수:
- 문자열을 특정 구분자를 기준으로 나누는 함수
- 기본값은 공백(스페이스)을 구분자로 사용
- 결과는 리스트(여러 값들의 모음)로 반환
"""

# 기본 split() 사용법
fruit = "사과 참외 수박".split()    # 공백을 기준으로 분리
print(f"분리 전: '사과 참외 수박'")
print(f"분리 후: {fruit}")
print(f"타입: {type(fruit)}")

"""
split() 동작 과정:
1. "사과 참외 수박" 문자열에서 공백을 찾음
2. 공백을 기준으로 문자열을 나눔
3. ['사과', '참외', '수박'] 리스트로 변환
"""

# 다른 구분자 사용 예시
text1 = "apple,banana,orange"
fruits_comma = text1.split(',')     # 쉼표를 구분자로 사용
