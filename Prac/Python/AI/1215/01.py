# 유사도 계산 (코사인 유사도)


def 코사인유사도(a, b):
    return np.dot(a, b) / (np.linalg.norm(a) * np.linalg.norm(b))


# 예: 영화 취향 벡터
# [액션, 로맨스, 코미디, 공포, SF]
사용자A = np.array([5, 1, 3, 0, 5])  # 액션, SF 좋아
사용자B = np.array([4, 0, 2, 1, 4])  # 비슷한 취향
사용자C = np.array([0, 5, 4, 0, 1])  # 로맨스 좋아함

print(f'A와 B유사도: {코사인유사도(사용자A, 사용자B)}')  # A와 B유사도: 0.9762956279494205
print(f'A와 C유사도: {코사인유사도(사용자A, 사용자C)}')  # A와 B유사도: 0.43825049008927763
print(f'B와 C유사도: {코사인유사도(사용자B, 사용자C)}')  # A와 B유사도: 0.30440777365910465

# 거리 계산
# 두 점 사이의 거리 = 벡터 뺼셈 후 크기
