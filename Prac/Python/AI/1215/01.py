# Numpy = Numeraical Python
# 수학 연산을 빠르고 쉽게 해주는 라이브러리

v1 = np.array([1, 2, 3])
print(v1)  # [1 2 3]

v2 = np.zeros(5)
print(v2)  # [0. 0. 0. 0. 0.]

# 벡터 정보 확인
# 벡터의 크기 (원소 개수)
print(len(v2))  # 5
print(v2.shape)  # (5,)

a = np.array([1, 2, 3])
b = np.array([2, 4, 6])
print(a * b)  # [ 2  8 18]

# 벡터의 크기 = 원점에서 해당 점까지의 거리
# 2차원 벡터 [3, 4]의 크기:
# 피타고라스 정리 사용
# 3**2 + 4**2 = 9 + 16 = 5**5 = 25

v = np.array([3, 4])
# 방법 1: 직접 계산
크기 = np.sqrt(v[0]**2 + v[1]**2)
print(크기)  # 5.0

# 방법 2: numpy 함수 사용(권장)
크기 = np.linalg.norm(v)
print(크기)  # 5.0

# 다차원 벡터도 같은 방법
v3 = np.array([1, 2, 2])
크기 = np.linalg.norm(v3)
print(크기)  # 3.0
크기 = np.sqrt(v3[0]**2 + v3[1]**2 + v3[2]**2)
print(크기)  # 3.0

# 단위 벡터: 크기가 1인 벡터
# 방향은 유지하고 크기만 1로 만듬

v = np.array([3, 4])

# 정규화: 벡터를 크기로 나눔
단위벡터 = v / np.linalg.norm(v)
print(단위벡터)  # [0.6 0.8]

print(np.linalg.norm(단위벡터))  # 1

