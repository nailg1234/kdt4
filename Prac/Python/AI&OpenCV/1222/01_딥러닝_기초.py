# 딥러닝 (Deep Learning)

# 쉽게 이해하기:
# 아기가 고양이를 배우는 과정을 생각해보세요.
# 1. 엄마가 "고양이야~" 하며 여러 고양이 사진을 보여줌
# 2. 아기는 점점 고양이의 특징을 기억함 (귀가 뾰족, 수염, 꼬리...)
# 3. 나중에 새로운 고양이를 봐도 "고양이!"라고 알아봄
# 딥러닝도 이와 똑같은 방식으로 학습합니다!

# 정의:
# 여러 층의 인공 신경망을 사용한 기계학습 방법

# 특징:
# - 다층 구조로 복잡한 패턴 학습
# - 특성 추출 자동화
# - 대용량 데이터에서 강력한 성능


# 왜 딥(Deep)??인가??

# 전통적 신경망:
# 입력 -> [은닉층 1개] -> 출력

# 딥러닝:
# 입력 -> [은닉층 1] -> [은닉층 2] -> [은닉층 3] ... [은닉층 N] -> 출력

# 층이 깊다(Deep) = 많은 은닉층
# -> 더 복잡한 패턴 학습 가능

# 실생활 비유:
# 사진을 보고 물체 인식하기
# 1층: 선과 가장자리 찾기
# 2층: 선들을 조합해 모양 찾기
# 3층: 모양들을 조합해 부분 찾기 (귀, 코, 눈)
# 4층: 부분들을 조합해 전체 인식 (고양이!)


# 머신러닝 vs 딥러닝

# 머신러닝:
# - 특성 추출: 사람이 직접 설계
#   예) "귀가 뾰족한가?", "수염이 있나?" 등을 프로그래머가 지정
# - 데이터: 적은 양도 가능 (수천 개)
# - 해석: 비교적 쉬움
# - 학습: 빠름

# 딥러닝:
# - 특성 추출: 모델이 자동 학습
#   예) 스스로 "귀 모양이 중요하구나" 발견
# - 데이터: 대용량 필요 (수만 개 이상)
# - 해석: 어려움 (블랙박스)
# - 학습: 느림 (GPU 필요)

# 비유:
# 머신러닝 = 요리책을 보고 요리 (레시피를 사람이 제공)
# 딥러닝 = 요리 영상을 많이 보고 스스로 배우기


# 특성 추출 비교 (이미지 분류 예시)

# 머신러닝 방식:
# 1. 전문가가 특성 설계 (모서리, 색상, 질감...)
# 2. 특성 추출 알고리즘 적용
# 3. 분류기 학습

# 딥러닝 방식:
# 1. 이미지 입력
# 2. 신경망이 자동으로 특성 학습
# 3. 분류 결과 출력
# -> End-to-End 학습


# 언제 무엇을 사용할까?

# 머신러닝 선택:
# - 데이터가 적을 때 (수천 개)
# - 해석이 중요할 때 (의료, 금융)
# - 컴퓨팅 자원 제한
# - 정형 데이터 (테이블 형식)

# 딥러닝 선택:
# - 데이터가 많을 때 (수만 개 이상)
# - 이미지, 텍스트, 음성 데이터
# - GPU 사용 가능
# - 최고 성능이 목표


# 딥러닝이 가능해진 이유

# 1. 데이터 폭발 (Data)
# - 인터넷, 스마트폰으로 대용량 데이터 생성
# - 예) 유튜브에 매일 500시간 분량의 영상 업로드

# 2. 컴퓨터 파워 (Computing Power)
# - GPU 병렬 처리 능력 향상
# - 클라우드 컴퓨팅 발전
# - 예전: 몇 주 → 지금: 몇 시간

# 3. 알고리즘 발전
# - 더 효율적인 학습 방법 개발
# - 과적합 방지 기법 (Dropout, Batch Normalization)


# 주요 용어 정리

# 신경망 (Neural Network):
# - 뇌의 뉴런을 모방한 계산 모델
# - 여러 층의 뉴런이 연결된 구조

# 층 (Layer):
# - 신경망의 각 단계 (여러 뉴런의 집합)
# - 입력층, 은닉층, 출력층

# 가중치 (Weight):
# - 입력과 출력 사이의 연결 강도
# - 학습을 통해 조정됨

# 파라미터 (Parameter):
# - 학습을 통해 조정되는 값 (가중치 + 편향)
# - 모델의 복잡도를 결정

# 에폭 (Epoch):
# - 전체 데이터를 한 번 학습하는 과정
# - 보통 여러 에폭 반복

# 배치 (Batch):
# - 한 번에 처리하는 데이터 묶음
# - 예) 1000장을 10장씩 나눠서 학습

# GPU (Graphics Processing Unit):
# - 병렬 연산에 특화된 하드웨어
# - 딥러닝 학습 속도를 크게 향상


# PyTorch 설치 확인
# PyTorch = 딥러닝을 쉽게 구현할 수 있는 프레임워크

import torch
import torchvision

print("=== PyTorch 설치 확인 ===")
print(f"PyTorch 버전: {torch.__version__}")
print(f"Torchvision 버전: {torchvision.__version__}")

# GPU 사용 가능 여부 확인
print(f"\nCUDA 사용 가능: {torch.cuda.is_available()}")
if torch.cuda.is_available():
    print("→ GPU를 사용할 수 있습니다! 학습이 빠릅니다.")
else:
    print("→ CPU로 학습합니다. (GPU보다 느릴 수 있습니다)")

# 간단한 텐서 연산
# 텐서 = 다차원 배열 (딥러닝의 기본 데이터 구조)
print("\n=== 간단한 텐서 만들기 ===")
x = torch.randn(3, 3)  # 3x3 크기의 랜덤 텐서
print("3x3 텐서 (랜덤 숫자):")
print(x)

# 텐서 연산 예시
print("\n=== 텐서 연산 ===")
a = torch.tensor([1, 2, 3])
b = torch.tensor([4, 5, 6])
print(f"a = {a}")
print(f"b = {b}")
print(f"a + b = {a + b}")
print(f"a * b = {a * b}")

print("\n✓ PyTorch가 정상적으로 작동합니다!")
